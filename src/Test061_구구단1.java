// 반복문(for 문) 실습
// 다중 for문 (반복문의 중첩)을 활용한 구구단 출력 실습
/*
실행 예)
[2단]
2 * 1 = 2
2 * 2 = 4
	:
2 * 9 = 18

[3단]
	:

9 * 9 = 81
계속하려면 아무 키나 누르세요.
*/
public class Test061_구구단1
{
	public static void main(String[] args)
	{
		int i, j;
		
//1) 구구단 세로
		System.out.println("1) 구구단 세로");
		for (i= 2; i <= 9; i++)
		{
			System.out.printf("===[%d단]===\n", i);

			for (j = 1; j <= 9; j++)
			{
				System.out.printf("%d x %d = %2d\n", i, j, (i * j));
				// (i*j) => 2자리 안넘어가니까 %2d 로 표현함
			}

			System.out.println();
		}
		System.out.println();


//1) 구구단 가로
		System.out.println("2) 구구단 가로");
		for (i = 1; i <= 9; i++)
		{

			for (j = 2; j <= 9; j++)
				System.out.printf("%d * %d = %2d  | ", j, i, (j * i));

			System.out.println();
		}
	}
}

